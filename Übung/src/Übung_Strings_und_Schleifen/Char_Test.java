package Übung_Strings_und_Schleifen;

public class Char_Test {
    public static void main(String[] args) {
        char a='a';
        System.out.println(++a);
        a='a';
        System.out.println(--a);
        a='ö';
        System.out.println(--a);
        a='1';
        System.out.println((int)a-48);
        a='2';
        System.out.println((int)a-48);
        a='3';
        System.out.println((int)a-48);
        a='4';
        System.out.println((int)a-48);
    }
}
